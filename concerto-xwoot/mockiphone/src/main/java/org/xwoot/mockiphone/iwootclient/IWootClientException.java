package org.xwoot.mockiphone.iwootclient;

public class IWootClientException extends Exception
{
    
    /**
     * 
     */
    private static final long serialVersionUID = 2360283191280119689L;

    public IWootClientException()
    {
        super();
    }

    public IWootClientException(Throwable arg0)
    {
        super(arg0);
    }

    public IWootClientException(String arg0)
    {
        super(arg0);
    }

    public IWootClientException(String arg0, Throwable t)
    {
        super(arg0, t);
    }
}
